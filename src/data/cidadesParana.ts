/**
 * Função para buscar cidades que correspondem ao termo de busca
 */
export function buscarCidadesSugeridas(termo: string): string[] {
  if (!termo || termo.length < 1) return [];

  const normalize = (s: string) => s
    .normalize('NFD')
    .replace(/[\u0300-\u036f]/g, '')
    .toLowerCase()
    .trim();

  const t = normalize(termo);

  const matches = cidadesParana.filter((cidade) => {
    const c = normalize(cidade);
    return c.startsWith(t) || c.includes(t);
  });

  // Priorizar resultados que começam com o termo
  const sorted = matches.sort((a, b) => {
    const aStarts = normalize(a).startsWith(t) ? 0 : 1;
    const bStarts = normalize(b).startsWith(t) ? 0 : 1;
    return aStarts - bStarts;
  });

  return sorted.slice(0, 8); // Limitar a 8 sugestões
}